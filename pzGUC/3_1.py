#1. Даны два целых числа: A, B. Проверить истинность высказывания: «Справедливы
#неравенства A > 0 или B < —2».

try:
    a = int(input("число а "))
    b = int(input("число б "))
except ValueError:
    print("ошибка ввода")
if a >= 0 or b <= 2:
    print("истина")
else:
    print("не истина")